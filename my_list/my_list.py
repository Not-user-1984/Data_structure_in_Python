# Создаем список с разными типами данных
my_list = [10, "Python", 3.14, [1, 2, 3]]

# Выводим весь список и его длину
print("Исходный список:", my_list)
print("Длина списка:", len(my_list))

# Доступ к элементам по индексу
print("Первый элемент:", my_list[0])  # 10
print("Вложенный список:", my_list[3])  # [1, 2, 3]
print("Элемент вложенного списка:", my_list[3][1])  # 2

# Изменение элемента
my_list[1] = "Go"  # Заменяем "Python" на "Grok"
print("После замены элемента:", my_list)

# Добавление элементов
my_list.append(42)  # Добавляем 42 в конец списка
print("После append:", my_list)

my_list.insert(2, "new")  # Вставляем "new" на индекс 2
print("После insert:", my_list)

# Удаление элементов
removed_element = my_list.pop()  # Удаляем последний элемент и сохраняем его
print("Удаленный элемент:", removed_element)
print("После pop:", my_list)

my_list.remove("new")  # Удаляем элемент "new"
print("После remove:", my_list)

# Работа со срезами
print("Срез [1:3]:", my_list[1:3])  # Элементы с индекса 1 до 2
print("Срез с шагом [::2]:", my_list[::2])  # Каждый второй элемент
print("Обратный список:", my_list[::-1])  # Список в обратном порядке

# Сортировка числового списка
numbers = [5, 2, 9, 1, 5, 6]
numbers.sort()  # Сортировка по возрастанию
print("Отсортированный список:", numbers)

numbers.sort(reverse=True)  # Сортировка по убыванию
print("Отсортированный по убыванию:", numbers)

# Цикл по списку
print("Элементы списка через цикл:")
for item in my_list:
    print(item, end=" ")  # Выводим элементы в строку

# Проверка наличия элемента
if "Go" in my_list:
    print("\n'Go' есть в списке!")
else:
    print("\n'Go' отсутствует в списке!")